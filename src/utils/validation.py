# Латиница часто используется как обход (транслит). Нормализуем сначала
# диграфы, потом одиночные буквы.

# Диграфы — комбинации латинских букв, кодирующих один кириллический звук.
# Применяются в первую очередь (порядок важен — сначала длинные!).
_LEET_DIGRAPHS = [
    ("shh", "щ"),
    ("yo",  "ё"), ("jo",  "ё"),
    ("yu",  "ю"), ("ju",  "ю"),
    ("ya",  "я"), ("ja",  "я"),
    ("ye",  "е"), ("je",  "е"),
    ("zh",  "ж"),
    ("kh",  "х"),
    ("ch",  "ч"),
    ("sh",  "ш"),
    ("ts",  "ц"),
]

# Одиночные буквы / цифры → кириллица.
# Транслит: фонетический («Petya» → «петя», а не визуальный «ретя»).
_LEET_MAP = str.maketrans({
    "@": "а", "a": "а",
    "0": "о", "o": "о",
    "3": "е", "e": "е",
    "u": "у", "y": "у",
    "i": "и", "1": "и",
    "p": "п",
    "x": "х",
    "k": "к",
    "c": "к",   # фонетический транслит «c» обычно «к» (Carrado→карадо)
    "s": "с",
    "b": "б",
    "h": "х",
    "m": "м",
    "t": "т",
    "n": "н",
    "g": "г",
    "z": "з",
    "j": "ж",
    "r": "р",
    "d": "д",
    "f": "ф",
    "l": "л",
    "v": "в",
    "w": "в",
    "q": "к",
})


# Слова-исключения (префиксы), в которых матные подстроки («еба», «ебу»)
# являются НЕматерью. При проверке: всё что начинается с одного из этих
# префиксов и продолжается буквами — стирается из нормализованной строки.
# Это надёжнее чем перечислять все падежные/числовые формы.
_PROFANITY_WHITELIST_PREFIXES = (
    "учеб",      # учеба, учебник, учебная, учебник, учебом
    "требов",    # требовать, требую, требует
    "ребус",     # ребус, ребусу, ребусы
    "жреби",     # жребий, жребия
    "погреб",    # погреба, погребу, погребом
    "хлеб",      # хлеба, хлебом
    "потреб",    # потребы, потребу
    "ущерб",     # ущерба, ущербу
    "герб",      # гербу, гербы (только если есть «у» — редко, но пусть)
    "себя", "себе",
    "судеб",     # судебная, судебном, судебной
    "лебед",     # лебедь, лебеди, лебединый
    "ангебот",
    "ребенк",    # ребенка
    "ребят",     # ребята, ребятам
    "потребн",   # потребности
    "оребр",     # оребрить (редко)
    "вребун",    # на всякий
    "погребен",  # погребен(ный)
    "побег",     # никак не относится — на всякий
)


def _strip_whitelisted(norm: str) -> str:
    """Убирает из нормализованной строки слова, начинающиеся с одного
    из whitelist-префиксов и продолжающиеся буквами/окончаниями.

    Пример: «учеба», «учебник», «судебная» — все стираются префиксом
    «учеб»/«судеб». «ученеба» же не стирается (там нет границы слова
    перед «учеб»)."""
    out = norm
    for prefix in sorted(_PROFANITY_WHITELIST_PREFIXES, key=len, reverse=True):
        # \b перед префиксом, затем сам префикс, затем 0+ букв до пробела
        # или конца строки
        pattern = rf"\b{re.escape(prefix)}[а-яё]*\b"
        out = re.sub(pattern, lambda m: " " * len(m.group()), out)
    return out


def _normalize(text: str) -> str:
    """Приводит текст к виду, удобному для поиска матных корней:
    - lower-case
    - сначала заменяет диграфы (sh→ш, kh→х, ya→я и т.д.)
    - потом одиночные latin-буквы и цифры на кириллицу по карте обходов
    - схлопывает повторяющиеся подряд буквы (хууууй → хуй)
    - убирает всё, кроме букв и пробелов (но «_» становится пробелом, чтобы
      «Bruce_Banner» не склеивалось в «брусебаннер» где «еба» создаёт ложный
      позитив).
    """
    t = text.lower().replace("_", " ")
    # Сначала диграфы — иначе sh схлопнется в «сн» по одиночным заменам.
    for src, dst in _LEET_DIGRAPHS:
        t = t.replace(src, dst)
    t = t.translate(_LEET_MAP)
    # Сжимаем подряд идущие одинаковые символы (>=3 → 1) для обхода 'хууууй'
    t = re.sub(r"(.)\1{2,}", r"\1", t)
    # Удаляем всё, кроме кириллицы и пробелов
    t = re.sub(r"[^а-яё ]", " ", t)
    # Сжимаем повторные пробелы
    t = re.sub(r"\s+", " ", t).strip()
    return t


_BAD_PATTERN = re.compile(r"|".join(_BAD_WORD_ROOTS))

# Маска самоцензуры: 2+ букв и 1+ "звёздочка-подобный" символ вперемешку,
# например 'бл***', 'х*й', 'с*ка', '****ишь', 'ху*' (буквы перед/после/между *).
_CENSORED_PATTERN = re.compile(
    r"(?:"
    r"[a-zа-яё]+[*#@+]+[a-zа-яё*#@+]*[a-zа-яё]"  # «х*й», «с*ка», «бл***ишь»
    r"|[*#@+]{2,}[a-zа-яё]+"                      # «**ишь»
    r"|[a-zа-яё]+[*#@+]{2,}"                      # «бл***»
    r"|[a-zа-яё]{2,}[*#@+]"                       # «ху*», «бл*»
    r")",
    re.IGNORECASE,
)


def contains_profanity(text: str) -> bool:
    """Возвращает True, если в тексте найден один из запрещённых корней
    или явная самоцензура мата (`х*й`, `бл***` и т.п.).
    """
    if not text:
        return False
    # 1. Проверка на самоцензуру: буквы вперемешку с */#/@/+
    if _CENSORED_PATTERN.search(text):
        return True
    # 2. Нормализованная проверка по корням
    norm = _normalize(text)
    if not norm:
        return False
    # Убираем слова-исключения («учеба», «требова» и т.п.), в которых
    # матные подстроки — нормальные слоги.
    norm = _strip_whitelisted(norm)
    return bool(_BAD_PATTERN.search(norm))


